package org.example.homework3;

public class MinMax {
    public static void main(String[] args) {
        int a = 3;
        int b = 7;

        int min = Math.min(a, b);
        int max = Math.max(a, b);

        System.out.println("Minimum: " + min);
        System.out.println("Maximum: " + max);
    }
}
